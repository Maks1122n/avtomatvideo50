        echo "  content/motivation/    - –º–æ—Ç–∏–≤–∞—Ü–∏–æ–Ω–Ω—ã–µ –≤–∏–¥–µ–æ"
        echo "  content/lifestyle/     - –ª–∞–π—Ñ—Å—Ç–∞–π–ª –∫–æ–Ω—Ç–µ–Ω—Ç"
        echo "  content/business/      - –±–∏–∑–Ω–µ—Å –∫–æ–Ω—Ç–µ–Ω—Ç"
        echo "  content/entertainment/ - —Ä–∞–∑–≤–ª–µ–∫–∞—Ç–µ–ª—å–Ω—ã–π –∫–æ–Ω—Ç–µ–Ω—Ç"
        echo ""
    else
        log_success "–ù–∞–π–¥–µ–Ω–æ $video_count –≤–∏–¥–µ–æ —Ñ–∞–π–ª–æ–≤"
    fi
}

