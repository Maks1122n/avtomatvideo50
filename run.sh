        echo ""
        echo "–°–æ–∑–¥–∞–Ω–∏–µ –ø–∞–ø–æ–∫..."
        mkdir -p content/{motivation,lifestyle,business,entertainment}
        log_success "–ü–∞–ø–∫–∏ —Å–æ–∑–¥–∞–Ω—ã"
    fi
    
    # –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞–ª–∏—á–∏—è –≤–∏–¥–µ–æ —Ñ–∞–π–ª–æ–≤
    video_count=0
    for dir in content/*/; do
        if [ -d "$dir" ]; then
            count=$(find "$dir" -name "*.mp4" -o -name "*.mov" | wc -l)
            video_count=$((video_count + count))
        fi
    done
    
    if [ $video_count -eq 0 ]; then
        log_warning "–í–∏–¥–µ–æ —Ñ–∞–π–ª—ã –Ω–µ –Ω–∞–π–¥–µ–Ω—ã –≤ –ø–∞–ø–∫–∞—Ö content/"
