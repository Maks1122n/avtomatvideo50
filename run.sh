    log_info "–ü—Ä–æ–≤–µ—Ä–∫–∞ –ø–∞–ø–æ–∫ —Å –∫–æ–Ω—Ç–µ–Ω—Ç–æ–º..."
    
    if [ ! -d "content" ]; then
